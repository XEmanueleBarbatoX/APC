/**
 * @brief  Rx Transfer completed callbacks.
 * @param  huart  Pointer to a UART_HandleTypeDef structure that contains
 *                the configuration information for the specified UART module.
 * @retval None
 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART1 || huart->Instance == USART6) {
		while (incidents_lock & INCIDENTS_LOCK_BIT) {
		}
		ATOMIC_SET_BIT(incidents_lock, INCIDENTS_LOCK_BIT); // acquire lock

		/* critical section */
		incidents++;

		ATOMIC_CLEAR_BIT(incidents_lock, INCIDENTS_LOCK_BIT); // release lock

		uint32_t now = __HAL_TIM_GET_COUNTER(&htim3);
		uint32_t next = (now + DELAY_TICKS) % (htim3.Init.Period + 1); // should be equal to now

		if (huart->Instance == USART1) {
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, next);
			HAL_TIM_OC_Start_IT(&htim3, TIM_CHANNEL_1);
		} else if (huart->Instance == USART6) {
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, next);
			HAL_TIM_OC_Start_IT(&htim3, TIM_CHANNEL_2);
		}
		HAL_UART_Receive_IT(huart, (huart->Instance == USART1) ? rx1 : rx6, 9);
	}
}

/**
 * @brief  Output Compare callback in non-blocking mode
 * @param  htim TIM OC handle
 * @retval None
 */
void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance != TIM3)
		return;
	uint8_t msg[] = "SNTHLP\r\n";
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) {
		// 3 s elapsed for TIM3 CH1, send on USART1
		HAL_UART_Transmit(&huart1, msg, sizeof(msg) - 1, HAL_MAX_DELAY);
		HAL_TIM_OC_Stop_IT(&htim3, TIM_CHANNEL_1);
	} else if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) {
		// 3 s elapsed for TIM3 CH2, send on USART6
		HAL_UART_Transmit(&huart6, msg, sizeof(msg) - 1, HAL_MAX_DELAY);
		HAL_TIM_OC_Stop_IT(&htim3, TIM_CHANNEL_2);
	}
}

