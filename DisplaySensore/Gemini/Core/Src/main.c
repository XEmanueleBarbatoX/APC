/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint8_t buf[12];
  HAL_StatusTypeDef ret, check;
  int16_t val;
  float temp_c;
  uint8_t msg[32];
  int len;
  uint8_t data_read[2];
  char temp_s[10];
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_LPUART1_UART_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  ssd1306_Init();

  /* USER CODE END 2 */

  /* Initialize led */
  BSP_LED_Init(LED_GREEN);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  ssd1306_Fill(White);
	  ssd1306_UpdateScreen();

	  buf[0] = REG_temp;
	  check = HAL_I2C_IsDeviceReady(&hi2c1, LMT75A_temp, 5, HAL_MAX_DELAY);
	  ret = HAL_I2C_Master_Transmit(&hi2c1, LMT75A_temp, buf, 1, HAL_MAX_DELAY);
	  if( ret != HAL_OK){
		 strcpy((char*)buf, "Error Tx\r\n");
		} else{
			ret = HAL_I2C_Master_Receive(&hi2c1, LMT75A_temp, data_read, 2, HAL_MAX_DELAY); // <--- AGGIUNTA FONDAMENTALE QUI!

			if (ret != HAL_OK) {
				strcpy((char*)buf, "Error Rx\r\n"); // Gestione errore di ricezione
			} else {
				// I dati di temperatura sono in data_read[0] (MSB) e data_read[1] (LSB)
				val = (int16_t)((data_read[0] << 8) | data_read[1]);

				// Shift a destra di 5 per ottenere i 11 bit significativi e l'estensione del segno
				val = val >> 5;

				// Calcolo della temperatura (1 LSB = 0.125Â°C)
				temp_c = (float)val * 0.125f;

				// Ora 'temp_c' contiene la temperatura corretta.
				// Formatta la stringa per la UART
				len = snprintf((char*)msg, sizeof(msg), "Temp: %.2f C\r\n", temp_c);
				HAL_UART_Transmit(&huart2, msg, len, HAL_MAX_DELAY);
				HAL_Delay(1000);

				sprintf(temp_s, "%.2f", temp_c);


				ssd1306_SetCursor(2, 12);
				ssd1306_WriteString(temp_s, Font_6x8, Black);
				ssd1306_UpdateScreen();


			}
		}



    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

